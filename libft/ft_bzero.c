#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (n > 0)
	{
		str[i++] = '\0';
		n--;
	}
	s = (void *)str;
}
